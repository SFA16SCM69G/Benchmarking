int main(int argc, char *argv[]) {
    FILE *fp;
    int rc;
    thread_num = atoi(argv[1]);
    printf("%d",thread_num);
    char *workload_name = argv[2];
    printf("%s\n",workload_name);
    
    char *filename[50];
    sprintf(filename,"./output/cpu_%d_%s.txt",thread_num,workload_name);
    
    fp = fopen(filename,"w");
    if(fp == NULL){
        printf("File is not found\n");
        return 0;
    }
    
    //Start time and end time of arithmetic
    struct timeval startTime, endTime;
    //Execution time
    double exec_time;
    pthread_t thread[thread_num];
    long threadId;
    void *status;
    
    gettimeofday(&startTime, NULL);
    //Create threads and start routine is determined by workload name
    for(threadId=0;threadId<thread_num;threadId++){
        if(strcmp(workload_name,"QP") == 0)
            pthread_create(&thread[threadId],NULL,char_arithmetic,(void*)threadId);
        else if(strcmp(workload_name,"HP") == 0)
            pthread_create(&thread[threadId],NULL,short_arithmetic,(void*)threadId);
        else if(strcmp(workload_name,"SP") == 0)
            pthread_create(&thread[threadId],NULL,int_arithmetic,(void*)threadId);
        else if(strcmp(workload_name,"DP") == 0)
            pthread_create(&thread[threadId],NULL,double_arithmetic,(void*)threadId);
        else{
            printf("Not a correct workload\n");
            return 0;
        }
    }
            
    //Block until all threads are finished
    for(threadId=0; threadId<thread_num; threadId++) {
        pthread_join(thread[threadId], &status);
    }
    gettimeofday(&endTime, NULL);
            
    //Compute execution time and processor speed
    exec_time = (double)((endTime.tv_usec - startTime.tv_usec)/1000000) +
    (double)(endTime.tv_sec - startTime.tv_sec);
    double speed = 1.0e12/exec_time/1.0e9;
    
    rc = fprintf(fp,"%s %d %s %s %s %f %s","Execution time of",thread_num,"threads and workload",workload_name,"is",exec_time,"seconds\n");
    if(rc < 0){
        printf("Write Failed\n");
        return 0;
    }
    rc = fprintf(fp,"%s %d %s %s %s %f %s","Processor speed of",thread_num,"threads and workload",workload_name,"is",speed,"GFLOPS\n");
    if(rc < 0){
        printf("Write Failed\n");
        return 0;
    }
    fclose(fp);
    printf("%s %d %s %s %s %f %s","Execution time of",thread_num,"threads and workload",workload_name,"is",exec_time,"seconds\n");
    printf("%s %d %s %s %s %f %s","Processor speed of",thread_num,"threads and workload",workload_name,"is",speed,"GFLOPS\n");
    
    return 0;
}

